class SemanticBertForAMRProbing(PreTrainedBertModel):
    """BERT model for token-level classification.
    This module is composed of the BERT model with a linear layer on top of
    the full hidden state of the last layer.

    Params:
        `config`: a BertConfig class instance with the configuration to build a new model.
        `num_labels`: the number of classes for the classifier. Default = 2.

    Inputs:
        `input_ids`: a torch.LongTensor of shape [batch_size, sequence_length]
            with the word token indices in the vocabulary(see the tokens preprocessing logic in the scripts
            `extract_features.py`, `run_classifier.py` and `run_squad.py`)
        `token_type_ids`: an optional torch.LongTensor of shape [batch_size, sequence_length] with the token
            types indices selected in [0, 1]. Type 0 corresponds to a `sentence A` and type 1 corresponds to
            a `sentence B` token (see BERT paper for more details).
        `attention_mask`: an optional torch.LongTensor of shape [batch_size, sequence_length] with indices
            selected in [0, 1]. It's a mask to be used if the input sequence length is smaller than the max
            input sequence length in the current batch. It's the mask that we typically use for attention when
            a batch has varying length sentences.
        `labels`: labels for the classification output: torch.LongTensor of shape [batch_size]
            with indices selected in [0, ..., num_labels].

    Outputs:
        if `labels` is not `None`:
            Outputs the CrossEntropy classification loss of the output with the labels.
        if `labels` is `None`:
            Outputs the classification logits of shape [batch_size, sequence_length, num_labels].

    Example usage:
    ```python
    # Already been converted into WordPiece token ids
    input_ids = torch.LongTensor([[31, 51, 99], [15, 5, 0]])
    input_mask = torch.LongTensor([[1, 1, 1], [1, 1, 0]])
    token_type_ids = torch.LongTensor([[0, 0, 1], [0, 1, 0]])

    config = BertConfig(vocab_size_or_config_json_file=32000, hidden_size=768,
        num_hidden_layers=12, num_attention_heads=12, intermediate_size=3072)

    num_labels = 2

    model = BertForTokenClassification(config, num_labels)
    logits = model(input_ids, token_type_ids, input_mask)
    ```
    """
    def __init__(self, config, num_labels):
        super(SemanticBertForAMRProbing, self).__init__(config)
        self.num_labels = num_labels
        self.hidden_size = 256
        # self.amr_relations = amr_relations

        self.bert = BertModel(config)
        self.hidden_ESRL = Interaction_2layer(config)

        self.reduce_predicate = nn.Linear(config.hidden_size, self.hidden_size)
        self.reduce_argument = nn.Linear(config.hidden_size, self.hidden_size)

        self.span_pooling = SelfAttentiveSpanExtractor(self.hidden_size)

        self.MLP = nn.Sequential(
            nn.Linear(self.hidden_size * 2, 4096),
            nn.ReLU(),
            nn.Linear(4096, self.hidden_size * 2),
            nn.ReLU()
        )

        # for attr in self.amr_relations:
        #     self.add_module("amr_relations_" + attr, torch.nn.Linear(self.hidden_size * 2, 1))
        self.classifier = nn.Linear(self.hidden_size * 2, self.num_labels)

        # self.loss_function = nn.BCELoss()
        self.loss_function = CrossEntropyLoss()

        self.apply(self.init_bert_weights)

    def forward(self, input_ids, span_indices_predicate, span_indices_argument,
                token_type_ids=None, attention_mask=None, labels=None):
        sequence_output, _ = self.bert(input_ids, token_type_ids, attention_mask, output_all_encoded_layers=False)
        # (B, T_s, H_w)
        extended_attention_mask = attention_mask.unsqueeze(1).unsqueeze(2)
        # Since attention_mask is 1.0 for positions we want to attend and 0.0 for
        # masked positions, this operation will create a tensor which is 0.0 for
        # positions we want to attend and -10000.0 for masked positions.
        # Since we are adding it to the raw scores before the softmax, this is
        # effectively the same as removing these entirely.
        extended_attention_mask = extended_attention_mask.to(dtype=next(self.parameters()).dtype)  # fp16 compatibility
        extended_attention_mask = (1.0 - extended_attention_mask) * -10000.0

        sequence_output = self.hidden_ESRL(sequence_output, extended_attention_mask)

        sequence_output_predicate = self.reduce_predicate(sequence_output)
        sequence_output_argument = self.reduce_argument(sequence_output)
        # (B, T_s, H)

        span_predicates = self.span_pooling(sequence_output_predicate, span_indices_predicate.unsqueeze(1))
        span_arguments = self.span_pooling(sequence_output_argument, span_indices_argument.unsqueeze(1))
        # (B, 1, H)

        # print(span_predicates.size())
        # print(span_arguments.size())
        x = torch.cat([span_predicates, span_arguments], 2).squeeze()
        # (B, 2H)
        # print(x.size())

        x = self.MLP(x)
        # (B, 2H)
        # print(x)

        logits = self.classifier(x)
        # logits = torch.sigmoid(self.classifier(x))
        # print(logits.size())
        # (B, C)
        # labels: (B, C)

        if labels is not None:
            # print('logits', torch.argmax(logits, dim=-1))
            # print('labels', labels)
            # print(torch.argmax(logits.view(-1, self.num_labels), dim=-1))
            # print(labels.view(-1))
            total_loss = self.loss_function(logits.view(-1, self.num_labels), labels.view(-1))
            # print(logits)
            # print(labels)
            # total_loss = self.loss_function(logits.view(-1), labels.view(-1).float())
            return total_loss
        else:
            # y_pred = (logits >= 0.5)
            # (B, C)
            # return y_pred
            return logits


